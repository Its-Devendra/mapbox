/**
 * Get default icon for a category based on name matching
 * @param {string} categoryName - Category name to match
 * @returns {object|null} - Matching icon object or default location icon
 */
export function getDefaultIconForCategory(categoryName) {
    if (!categoryName) return PROFESSIONAL_ICONS[0]; // Default to location

    const lowerName = categoryName.toLowerCase();

    // Find best matching icon based on keywords
    for (const icon of PROFESSIONAL_ICONS) {
        for (const keyword of icon.keywords) {
            if (lowerName.includes(keyword) || keyword.includes(lowerName)) {
                return icon;
            }
        }
    }

    // Return default location icon if no match
    return PROFESSIONAL_ICONS[0];
}

